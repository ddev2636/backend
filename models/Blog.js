import mongoose from "mongoose";

const BlogSchema = new mongoose.Schema(
  {
    firstName: {
      type: String,
      required: true,
      min: 2,
      max: 50,
    },
    lastName: {
      type: String,
      required: true,
      min: 2,
      max: 50,
    },
    email: {
      type: String,
      required: true,
      max: 50,
      unique: true,
    },
    password: {
      type: String,
      required: true,
      min: 5,
    },
    picture: {
      type: String,
      default: "",
    },
  },
  { timestamps: true }
);

const User = mongoose.model("Blog", BlogSchema);
export default User;
